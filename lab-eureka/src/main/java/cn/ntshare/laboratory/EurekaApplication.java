/**
 * Created By Q.Hao
 * Description:
 * Created At 2019/5/19
 */
@SpringBootApplication
@EnableEurekaServer
public class EurekaApplication {

    public static void main(String[] args) {
        SpringApplication.run(EurekaApplication.class, args);
    }
}
